// The trimStart() method works like trim(), but removes whitespaces only from the start of a string.


let text1="          Dhananjaya";
console.log(text1);
console.log(text1.length);
let text2=text1.trimStart();
console.log(text2);
console.log(text2.length);

/*Output:
    Dhananjaya
20
Dhananjaya
10
*/