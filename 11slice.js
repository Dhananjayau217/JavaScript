// The slice() method slices out a piece of an array into a new array.

let numbers=[1,2,3,4,5,6];
let string=["dhana","pavan","shiva","sheshu","uma","dharshan"];
let num1=numbers.slice(1,4);
let num2=string.slice(1,4);
console.log(num1);
console.log(num2);

/*Output:

[ 2, 3, 4 ]
[ 'pavan', 'shiva', 'sheshu' ]

*/