var num=110;
if(num%5==0 && num%11==0){
    console.log(`${num} is divisible by both 5 and 11`);
}else{
    console.log(`${num} is not divisible by both 5 and 11`);
}

/*Output:
110 is divisible by both 5 and 11
*/

